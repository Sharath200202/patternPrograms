public class Pattern1 {

	public static void main(String[] args) {
		/*Using for loop
		    for(int i=1;i<=5;i++) {
			System.out.print("*");
		}*/
		/*Using While loop
		int i=1;
		while(i<=5) {
			System.out.print("*");
			i++;
		}
		*/
		//Using Do-while loop
		int i=1;
		do {
			System.out.print("*");
			i++;
		}while(i<=5);
		

	}

}
